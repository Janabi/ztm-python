# int & float
# float gets more space in memory than int, since it stores two different locations one before floating point and one after it.

print(type(2 + 4))
print(type(2 - 4))
print(type(2 * 4))
print(type(2 / 4))

print(type(0.0001))
print(type(0))
print(type(20+1.1))
print(9.9+1.1)

print(2 ** 2) # 2 ^ 2 = 4
print(2 // 4) # rounded down into nearest integer
print(6 % 4) # modula, reminder of 4